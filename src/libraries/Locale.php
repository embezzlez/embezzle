  public  function langcountry($code)
  {

    #----
    $code = strtolower($code);
    if ($code == 'eu') {
      return 'en';
    } elseif ($code == 'ap') { # Asia Pacific
      return 'en';
    } elseif ($code == 'cs') {
      return 'sr';
    } elseif ($code == 'uk') {
      $code = 'en';
    }
    if (array_key_exists($code, $this->locale_list)) {
      if (strpos($this->locale_list[$code], ',') !== false) {
        $new = explode(',', $this->locale_list[$code]);
        $loc = array();
        foreach ($new as $key => $val) {
          $loc[] = $val;
        }
        /*return implode(',', $loc); # string; comma-separated values 'en_GB,ga_GB,cy_GB,gd_GB,kw_GB'*/
        return $loc[0];
      } else {
        return $this->locale_list[$code];
      }
    }
    return false;
  }

